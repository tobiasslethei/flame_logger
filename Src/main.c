typedef struct {
  float tc_temp;
  float internal_temp;
  uint8_t fault;
  uint8_t scv, scg, oc;
} MAX31855_Data;

static uint16_t servo_pulse = 1500U;
static uint8_t motor_enabled = 1U;

static MAX31855_Data Read_MAX31855(void) {
  MAX31855_Data d = {0};
  uint8_t data[4];

  HAL_GPIO_WritePin(MAX31855_CS_GPIO_Port, MAX31855_CS_Pin, GPIO_PIN_RESET);
  HAL_SPI_Receive(&hspi4, data, 4, HAL_MAX_DELAY);
  HAL_GPIO_WritePin(MAX31855_CS_GPIO_Port, MAX31855_CS_Pin, GPIO_PIN_SET);

  uint32_t raw = ((uint32_t)data[0] << 24) |
                 ((uint32_t)data[1] << 16) |
                 ((uint32_t)data[2] << 8) |
                 ((uint32_t)data[3]);

  int16_t tc_raw = (raw >> 18) & 0x3FFF;
  if (tc_raw & 0x2000) {
    tc_raw |= 0xC000;
  }
  d.tc_temp = tc_raw * 0.25f;

  int16_t int_raw = (raw >> 4) & 0x0FFF;
  if (int_raw & 0x0800) {
    int_raw |= 0xF000;
  }
  d.internal_temp = int_raw * 0.0625f;

  d.fault = (raw >> 16) & 0x01;
  d.scv = (raw >> 2) & 0x01;
  d.scg = (raw >> 1) & 0x01;
  d.oc = raw & 0x01;

  return d;
}

static void ServoWrite(uint16_t pulse) {
  if (pulse < 500U) {
    pulse = 500U;
  } else if (pulse > 2500U) {
    pulse = 2500U;
  }
  servo_pulse = pulse;
  if (motor_enabled) {
    __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_3, servo_pulse);
  }
}

static void MotorSet(uint8_t enable) {
  if (enable) {
    if (!motor_enabled) {
      HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_3);
      motor_enabled = 1U;
      __HAL_TIM_SET_COMPARE(&htim4, TIM_CHANNEL_3, servo_pulse);
    }
  } else {
    if (motor_enabled) {
      HAL_TIM_PWM_Stop(&htim4, TIM_CHANNEL_3);
      motor_enabled = 0U;
    }
  }
}

static void SendData(const MAX31855_Data *d) {
  char buf[96];

  if (d->fault) {
    snprintf(buf, sizeof buf, "DATA,%lu,%u,FAULT,%.2f,%.2f,%d,%d,%d\r\n",
             HAL_GetTick(),
             (unsigned int)servo_pulse,
             d->tc_temp,
             d->internal_temp,
             d->scv,
             d->scg,
             d->oc);
  } else {
    snprintf(buf, sizeof buf, "DATA,%lu,%u,%.2f,%.2f\r\n",
             HAL_GetTick(),
             (unsigned int)servo_pulse,
             d->tc_temp,
             d->internal_temp);
  }

  HAL_UART_Transmit(&huart3, (uint8_t *)buf, strlen(buf), HAL_MAX_DELAY);
}

